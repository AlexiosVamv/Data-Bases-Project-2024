    private void select_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_select_btnActionPerformed
        this.setVisible(false);
        if ("Etaireia".equals((String)dropdown_menu.getSelectedItem())){            
            new Etaireia_Edit().setVisible(true);
        }   
        else if("User".equals((String)dropdown_menu.getSelectedItem())){
         new User_Edit().setVisible(true);
        }
        else if("Evaluator".equals((String)dropdown_menu.getSelectedItem())){
         new Evaluator_Edit().setVisible(true);
        }
        else if("Job".equals((String)dropdown_menu.getSelectedItem())){
         new Job_Edit().setVisible(true);
        }
        else if("Employee".equals((String)dropdown_menu.getSelectedItem())){
         new Employee_Edit().setVisible(true);
        }
        else if("Subject".equals((String)dropdown_menu.getSelectedItem())){
         new Subject_Edit().setVisible(true);
        }
        else if("Requires".equals((String)dropdown_menu.getSelectedItem())){
         new Requires_Edit().setVisible(true);
        }
        else if("Languages".equals((String)dropdown_menu.getSelectedItem())){
         new Lang_Edit().setVisible(true);
        }
        else if("Project".equals((String)dropdown_menu.getSelectedItem())){
         new Project_Edit().setVisible(true);
        }
        else if("Applies".equals((String)dropdown_menu.getSelectedItem())){
         new Applies_Edit().setVisible(true);
        }
        else if("Degree".equals((String)dropdown_menu.getSelectedItem())){
         new Degree_Edit().setVisible(true);
        }
        else if("Has_degree".equals((String)dropdown_menu.getSelectedItem())){
         new Has_Degree_Edit().setVisible(true);
        }
        else if("Job_Employee".equals((String)dropdown_menu.getSelectedItem())){
         new Job_Employee().setVisible(true);
        }
        else if("History_Applies".equals((String)dropdown_menu.getSelectedItem())){
         new HistoryApplies().setVisible(true);
        }
        
        
        
    }//GEN-LAST:event_select_btnActionPerformed

