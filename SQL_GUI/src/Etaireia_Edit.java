    private void btnEtaireia_DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEtaireia_DeleteActionPerformed
        DefaultTableModel RecordTable = (DefaultTableModel)Etaireia_Table.getModel();
        int SelectedRows = Etaireia_Table.getSelectedRow();
        
        try{
            
            int AFM;
            AFM = Integer.parseInt(RecordTable.getValueAt(SelectedRows, 0).toString());
            
             deleteItem = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this entry ?","Warning!",JOptionPane.YES_NO_OPTION);
            if (deleteItem == JOptionPane.YES_OPTION){
                Class.forName("com.mysql.jdbc.Driver");
               Connection con = DriverManager.getConnection(DB_URL,username,password);
                PreparedStatement stmnt = con.prepareStatement("DELETE FROM etaireia WHERE AFM = ?");
                
                stmnt.setLong(1, AFM);
                stmnt.executeUpdate();
                JOptionPane.showMessageDialog(this,"Entry Deleted");
                UpdateDB();
                
                txtEtaireia_AFM.setText("");
                txtEtaireia_AFM.requestFocus();
                txtEtaireia_DOY.setText("");
                txtEtaireia_Name.setText("");
                txtEtaireia_Telephone.setText("");
                txtEtaireia_Street.setText("");
                txtEtaireia_Num.setText("");
                txtEtaireia_City.setText("");
                txtEtaireia_Country.setText("");
            }
        }catch (ClassNotFoundException ex){
            java.util.logging.Logger.getLogger(Etaireia_Edit.class.getName()).log(java.util.logging.Level.SEVERE ,null,ex);
        }catch (SQLException ex){
            System.err.println(ex);
        }
    }//GEN-LAST:event_btnEtaireia_DeleteActionPerformed

