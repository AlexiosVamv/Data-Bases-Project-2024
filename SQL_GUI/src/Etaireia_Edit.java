    public  void UpdateDB() {
        
        try{
            Class.forName("com.mysql.jdbc.Driver");
           Connection con = DriverManager.getConnection(DB_URL,username,password);
            
            PreparedStatement stmnt = con.prepareStatement("SELECT * FROM etaireia");
            
            ResultSet resultSet = stmnt.executeQuery();
            
            ResultSetMetaData metadata = resultSet.getMetaData();
            int numberOfColumns = metadata.getColumnCount();
            
            DefaultTableModel RecordTable = (DefaultTableModel)Etaireia_Table.getModel();
                    RecordTable.setRowCount(0);
                    
             while (resultSet.next()){
                 
             Vector columnData = new Vector();
                for (int i = 1; i <= numberOfColumns; i++){
                    columnData.add(resultSet.getLong("AFM"));
                    columnData.add(resultSet.getString("DOY"));
                    columnData.add(resultSet.getString("name"));
                    columnData.add(resultSet.getLong("tel"));
                    columnData.add(resultSet.getString("street"));
                    columnData.add(resultSet.getInt("num"));
                    columnData.add(resultSet.getString("city"));
                    columnData.add(resultSet.getString("country"));
                }                 
                RecordTable.addRow(columnData);
             }
       
        }catch (Exception ex) {
             JOptionPane.showMessageDialog(null, ex);
        }
     } 
