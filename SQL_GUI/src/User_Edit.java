    private void User_TableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_User_TableMouseClicked
        DefaultTableModel RecordTable = (DefaultTableModel)User_Table.getModel();
        int SelectedRows = User_Table.getSelectedRow();

        txtUser_username.setText(RecordTable.getValueAt(SelectedRows, 0).toString());
        txtUser_password.setText(RecordTable.getValueAt(SelectedRows, 1).toString());
        txtUser_Name.setText(RecordTable.getValueAt(SelectedRows, 2).toString());
        txtUser_Lastname.setText(RecordTable.getValueAt(SelectedRows, 3).toString());
        try{
            Date reg_date = new SimpleDateFormat("yyyy-MM-dd").parse(RecordTable.getValueAt(SelectedRows, 4).toString());
        txtUser_reg_date.setDate(reg_date);
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        txtUser_email.setText(RecordTable.getValueAt(SelectedRows, 5).toString());
        
        txtUser_username.disable();
    }//GEN-LAST:event_User_TableMouseClicked

    private void btnUser_DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUser_DeleteActionPerformed
        DefaultTableModel RecordTable = (DefaultTableModel)User_Table.getModel();
        int SelectedRows = User_Table.getSelectedRow();

        try{

            String usernm;
            usernm = (RecordTable.getValueAt(SelectedRows, 0).toString());

            deleteItem = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this entry ?","Warning!",JOptionPane.YES_NO_OPTION);
            if (deleteItem == JOptionPane.YES_OPTION){
                Class.forName("com.mysql.jdbc.Driver");
                Connection con = DriverManager.getConnection(DB_URL,username,password);
                PreparedStatement stmnt = con.prepareStatement("DELETE FROM user WHERE username = ?");

                stmnt.setString(1, usernm);
                stmnt.executeUpdate();
                JOptionPane.showMessageDialog(this,"Entry Deleted");
                UpdateDB();

                txtUser_username.setText("");
                txtUser_username.requestFocus();
                txtUser_password.setText("");
                txtUser_Name.setText("");
                txtUser_Lastname.setText("");
                txtUser_reg_date.setDate(null);
                txtUser_email.setText("");
                
            }
        }catch (ClassNotFoundException ex){
            java.util.logging.Logger.getLogger(Etaireia_Edit.class.getName()).log(java.util.logging.Level.SEVERE ,null,ex);
        }catch (SQLException ex){
            System.err.println(ex);
        } 
    }//GEN-LAST:event_btnUser_DeleteActionPerformed

    private void btnUser_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUser_UpdateActionPerformed
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection(DB_URL,username,password);
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String reg_date = sdf.format(txtUser_reg_date.getDate());
            PreparedStatement stmnt = con.prepareStatement("UPDATE user SET password = ?, name = ?,lastname = ?,reg_date = ?,email = ? where username = ?");

            stmnt.setString(1,txtUser_password.getText());
            stmnt.setString(2,txtUser_Name.getText());
            stmnt.setString(3,txtUser_Lastname.getText());
            stmnt.setString(4,reg_date);
            stmnt.setString(5,txtUser_email.getText());
            stmnt.setString(6,txtUser_username.getText());

            stmnt.executeUpdate();

            JOptionPane.showMessageDialog(this, "Record Updated");
            UpdateDB();
        }catch (ClassNotFoundException ex){
            java.util.logging.Logger.getLogger(User_Edit.class.getName()).log(java.util.logging.Level.SEVERE ,null,ex);
        }catch (SQLException ex){
            java.util.logging.Logger.getLogger(User_Edit.class.getName()).log(java.util.logging.Level.SEVERE ,null,ex);
        }
    }//GEN-LAST:event_btnUser_UpdateActionPerformed

