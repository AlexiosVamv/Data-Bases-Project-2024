    private void btnEtaireia_DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEtaireia_DeleteActionPerformed
        DefaultTableModel RecordTable = (DefaultTableModel)Has_Degree_Table.getModel();
        int SelectedRows = Has_Degree_Table.getSelectedRow();

        try{
            
            

            deleteItem = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this entry ?","Warning!",JOptionPane.YES_NO_OPTION);
            if (deleteItem == JOptionPane.YES_OPTION){
                Class.forName("com.mysql.jdbc.Driver");
                Connection con = DriverManager.getConnection(DB_URL,username,password);
                PreparedStatement stmnt = con.prepareStatement("DELETE FROM has_degree WHERE degr_title = ? and degr_idryma = ? and cand_usrname = ?");

                stmnt.setString(1,RecordTable.getValueAt(SelectedRows, 0).toString());
                stmnt.setString(2,RecordTable.getValueAt(SelectedRows, 1).toString());
                stmnt.setString(3,RecordTable.getValueAt(SelectedRows, 2).toString());
                stmnt.executeUpdate();
                JOptionPane.showMessageDialog(this,"Entry Deleted");
                UpdateDB();

                txtHDeg_titlos.setSelectedItem(null);
                txtHDeg_idryma.setSelectedItem(null);
                txtHDeg_employee.setSelectedItem(null);
                txtHDeg_etos.setYear(2024);
                txtHDeg_grade.setText("");

            }
        }catch (ClassNotFoundException ex){
            java.util.logging.Logger.getLogger(Project_Edit.class.getName()).log(java.util.logging.Level.SEVERE ,null,ex);
        }catch (SQLException ex){
            System.err.println(ex);
        }
    }//GEN-LAST:event_btnEtaireia_DeleteActionPerformed

