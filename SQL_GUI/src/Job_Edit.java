    private void Job_TableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Job_TableMouseClicked
        DefaultTableModel RecordTable = (DefaultTableModel)Job_Table.getModel();
        int SelectedRows = Job_Table.getSelectedRow();
                
        txtJob_ID.setText(RecordTable.getValueAt(SelectedRows, 0).toString());
        try{
        Date start_date  = new SimpleDateFormat("yyyy-MM-dd").parse(RecordTable.getValueAt(SelectedRows, 1).toString());
        txtJob_startdate.setDate(start_date);  
        }catch(Exception e){
            JOptionPane.showInternalMessageDialog(null,e);
        }
        txtJob_salary.setText(RecordTable.getValueAt(SelectedRows, 2).toString());
        txtJob_position.setText(RecordTable.getValueAt(SelectedRows, 3).toString());
        txtJob_edra.setText(RecordTable.getValueAt(SelectedRows, 4).toString());
        dropdJob_evaluator.setSelectedItem(RecordTable.getValueAt(SelectedRows, 5).toString());
        try{
            Date announce_date  = new SimpleDateFormat("yyyy-MM-dd").parse(RecordTable.getValueAt(SelectedRows, 6).toString());
            Date sub_date  = new SimpleDateFormat("yyyy-MM-dd").parse(RecordTable.getValueAt(SelectedRows, 7).toString());
        txtJob_announcedate.setDate(announce_date);
        txtJob_subdate.setDate(sub_date);
        }catch (Exception e){
            JOptionPane.showInternalMessageDialog(null,e);
        }
        
        txtJob_ID.disable();
    }//GEN-LAST:event_Job_TableMouseClicked

    private void btnEtaireia_DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEtaireia_DeleteActionPerformed
        DefaultTableModel RecordTable = (DefaultTableModel)Job_Table.getModel();
        int SelectedRows = Job_Table.getSelectedRow();

        try{

            int id;
            id = Integer.parseInt(RecordTable.getValueAt(SelectedRows, 0).toString());

            deleteItem = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this entry ?","Warning!",JOptionPane.YES_NO_OPTION);
            if (deleteItem == JOptionPane.YES_OPTION){
                Class.forName("com.mysql.jdbc.Driver");
                Connection con = DriverManager.getConnection(DB_URL,username,password);
                PreparedStatement stmnt = con.prepareStatement("DELETE FROM job WHERE id = ?");

                stmnt.setLong(1, id);
                stmnt.executeUpdate();
                JOptionPane.showMessageDialog(this,"Entry Deleted");
                UpdateDB();

                txtJob_ID.setText("");
                txtJob_ID.requestFocus();
                txtJob_startdate.setDate(null);
                txtJob_salary.setText("");
                txtJob_position.setText("");
                txtJob_edra.setText("");
                dropdJob_evaluator.setSelectedItem(null);
                txtJob_announcedate.setDate(null);
                txtJob_subdate.setDate(null);
            }
        }catch (ClassNotFoundException ex){
            java.util.logging.Logger.getLogger(Job_Edit.class.getName()).log(java.util.logging.Level.SEVERE ,null,ex);
        }catch (SQLException ex){
            System.err.println(ex);
        }
    }//GEN-LAST:event_btnEtaireia_DeleteActionPerformed

