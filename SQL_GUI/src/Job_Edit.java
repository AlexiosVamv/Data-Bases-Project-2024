    public  void UpdateDB() {
        
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection(DB_URL,username,password);
            
            PreparedStatement stmnt = con.prepareStatement("SELECT * FROM job");
            
            ResultSet resultSet = stmnt.executeQuery();
            
            ResultSetMetaData metadata = resultSet.getMetaData();
            int numberOfColumns = metadata.getColumnCount();
            
            DefaultTableModel RecordTable = (DefaultTableModel)Job_Table.getModel();
                    RecordTable.setRowCount(0);
                    
             while (resultSet.next()){
                 
             Vector columnData = new Vector();
                for (int i = 1; i <= numberOfColumns; i++){
                    columnData.add(resultSet.getString("id"));
                    columnData.add(resultSet.getDate("start_date"));
                    columnData.add(resultSet.getString("position"));
                    columnData.add(resultSet.getInt("salary"));
                    columnData.add(resultSet.getString("edra"));
                    columnData.add(resultSet.getString("evaluator"));
                    columnData.add(resultSet.getDate("announce_date"));
                    columnData.add(resultSet.getDate("submission_date"));
                    
                }                 
                RecordTable.addRow(columnData);
             }
       
        }catch (Exception ex) {
             JOptionPane.showMessageDialog(null, ex);
        }
     } 
    
