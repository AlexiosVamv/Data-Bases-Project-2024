    private void btn_DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DeleteActionPerformed
        DefaultTableModel RecordTable = (DefaultTableModel)Subject_Table.getModel();
        int SelectedRows = Subject_Table.getSelectedRow();

        try{

            String title;
            title = (RecordTable.getValueAt(SelectedRows, 0).toString());

            deleteItem = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this entry ?","Warning!",JOptionPane.YES_NO_OPTION);
            if (deleteItem == JOptionPane.YES_OPTION){
                Class.forName("com.mysql.jdbc.Driver");
                Connection con = DriverManager.getConnection(DB_URL,username,password);
                PreparedStatement stmnt = con.prepareStatement("DELETE FROM subject WHERE title = ?");

                stmnt.setString(1, title);
                stmnt.executeUpdate();
                JOptionPane.showMessageDialog(this,"Entry Deleted");
                UpdateDB();

                txtSubject_title.setText("");
                txtSubject_title.requestFocus();
                txtSubject_descr.setText("");
                Subject_Combo.setSelectedItem(null);
                        
                }
        }catch (ClassNotFoundException ex){
            java.util.logging.Logger.getLogger(Subject_Edit.class.getName()).log(java.util.logging.Level.SEVERE ,null,ex);
        }catch (SQLException ex){
            System.err.println(ex);
        }
    }//GEN-LAST:event_btn_DeleteActionPerformed

